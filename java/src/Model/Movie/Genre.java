package Model.Movie;

/**
 * The Enum Genre, each subtype representing a movie genre. For example: Action , Comedy, Horror
 */

public enum Genre {
	
	/** Action movie genre */
    ACTION,
    
    /** Comedy movie genre */
    COMEDY,
    
    /** Romance movie genre */
    ROMANCE,
    
    /** Horror movie genre */
    HORROR,
    
    /** Adventure movie genre */
    ADVENTURE,
    
    /** Mystery movie genre */
    MYSTERY,
    
    /** Animation movie genre */
    ANIMATION;
}
